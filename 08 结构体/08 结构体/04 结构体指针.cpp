#include<iostream>
using namespace std;
#include<string>

//结构体指针

//定义学生结构体
struct Student
{
	string name;

	int age;

	int score;
};

int main2()
{
	//1.创建学生结构体变量
	struct Student s;
	s.name = "张三";
	s.age = 18;
	s.score = 100;

	//2.通过指针指向结构体变量
	struct Student* p = &s;

	//3.通过指针访问结构体变量中的数据
	cout << "姓名： " << p->name
		<< " 年龄： " << p->age
		<< " 成绩： " << p->score << endl;

	system("pause");

	return 0;
}